// 1. Arithmetic Operators

let result;

result = 5 + 5;
result = 5 - 5;
result = 5 * 5;
result = 5 / 5;
result = 7 % 5;

// Concatenation
result = 'Hello' + ' ' + 'World';

// Exponent
result = 2 ** 3;

// Increment
result = 1;
// result = result + 1;
result++;

// Decrement
// result = result - 1;
result--;

// 2. Assignment Operators

result = 10;

result += 5;
result -= 5;
result *= 5;
result /= 5;
result %= 5;
result **= 5;

// 3. Comparison Operators

// Equal to (Just the value, not the type)
result = 2 == '2';

// Equal to (Type and value)
result = 2 === '2';

// Not equal to (Just the value, not the type)
result = 2 != '2';

// Not equal to (Type and value)
result = 2 !== 2;

// Greater than and less than
result = 10 > 5;
result = 10 < 5;
result = 10 <= 5;
result = 10 >= 5;

console.log(result);
